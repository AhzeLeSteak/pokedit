import {Language} from "../../firebase/types";

export function UInt8ToPKChar(uint: number, language: Language){
    switch (language){
        case 'FR':
        case 'GR':
            return FRENCH_CHAR[uint-0x80];
        case 'EN':
            return ENGLISH_CHAR[uint-0x80];
        case 'JP':
            return JAP_CHAR[uint];
        case "IT":
        case "SP":
            return ITALIAN_CHAR[uint-0x80]
    }
    return '';
}

export function PKCharToUInt8(char: string,language: Language){
    switch (language){
        case 'FR':
        case 'GR':
            return FRENCH_CHAR.indexOf(char) + 0x80;
        case 'EN':
            return ENGLISH_CHAR.indexOf(char) + 0x80;
        case 'JP':
            return JAP_CHAR.indexOf(char);
        case "IT":
        case "SP":
            return ITALIAN_CHAR.indexOf(char) + 0x80
    }
    return 0;
}

